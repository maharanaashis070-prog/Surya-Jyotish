export const CITIES = RAW.map(([name, admin, country, lat, lon]) => ({
  name, admin, country, lat, lon,
  label: admin ? `${name}, ${admin}, ${country}` : `${name}, ${country}`,
}));

export function searchCities(query, limit = 8) {
  const q = (query || '').trim().toLowerCase();
  if (q.length < 1) return [];
  const starts = [];
  const contains = [];
  for (const c of CITIES) {
    const hay = c.label.toLowerCase();
    if (hay.startsWith(q) || c.name.toLowerCase().startsWith(q)) starts.push(c);
    else if (hay.includes(q)) contains.push(c);
    if (starts.length >= limit) break;
  }
  return [...starts, ...contains].slice(0, limit);
}
